def run_test_5_pulling(args, lvc_truth=False):
    """Test 5 / 5b: BAO-position-shift sweep, BAO+DP+SH0ES only."""
    label = "5b" if lvc_truth else "5"
    truth = TRUTH_LVC if lvc_truth else TRUTH_NULL
    print("\n" + "=" * 70)
    print(f"TEST {label}: BAO-position-shift sweep, "
          f"{'LVC' if lvc_truth else 'null'} truth")
    print("=" * 70)
    deltas = [-0.10, -0.05, 0.0, +0.05, +0.10]
    results = []
    bao_base, _, dp_base, h0_base = generate_mock(
        truth, seed=42, lvc_truth=lvc_truth, with_pp=False)
    for dz in deltas:
        # Apply z-shift to all BAO tables (variable layouts handled by length)
        bao_shifted = {}
        for k, table in bao_base.items():
            new = []
            for entry in table:
                if len(entry) == 7:                     # PAIR table
                    label_, z, DM, sDM, DH, sDH, corr = entry
                    new.append((label_, z + dz, DM, sDM, DH, sDH, corr))
                elif len(entry) == 4 and isinstance(entry[0], str):  # eBOSS DV
                    label_, z, DV, sig = entry
                    new.append((label_, z + dz, DV, sig))
                elif len(entry) == 4:                   # BOSS_DH
                    z, DH, sig, corr = entry
                    new.append((z + dz, DH, sig, corr))
                elif len(entry) == 3:                   # DESI_DV or BOSS_DM
                    z, val, sig = entry
                    new.append((z + dz, val, sig))
                else:
                    new.append(entry)
            bao_shifted[k] = new
        r = fit_family(bao_shifted, None, dp_base, h0_base,
                       "F1_zGauss", include_pp=False,
                       n_seeds=args.n_seeds_per_fit, de_iter=args.de_iter)
        zc_fit = r["params"][4]
        results.append({"delta_z": dz, "f1_zc": zc_fit, "f1_A": r["params"][3]})
        print(f"  delta_z = {dz:+.2f}: F1 zc = {zc_fit:.4f}, "
              f"A = {r['params'][3]:+.4f}")

    dzs = np.array([r["delta_z"] for r in results])
    zcs = np.array([r["f1_zc"]   for r in results])
    if len(dzs) > 1:
        slope = float(np.polyfit(dzs, zcs, 1)[0])
        verdict = "~ 0, no pulling" if abs(slope) < 0.1 else "BAO pulling detected"
        print(f"  Linear slope dz_c/d(delta_z) = {slope:+.3f}  ({verdict})")
        print("  Paper value (Test 5b): -0.022")
    return results


