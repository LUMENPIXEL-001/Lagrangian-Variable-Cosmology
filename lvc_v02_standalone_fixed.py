def _derive_rd(H0, Om, Obh2, T_func=None):
    """Derive r_d from Planck theta* via geometric construction.
    DM_at_zstar in v0.1 takes (H0, Om, Ob_h2, T_func=None)."""
    DMz = DM_at_zstar(H0, Om, Obh2, T_func)
    return THETA_STAR * DMz


def _build_mock_pp(truth_params, T_truth, rng):
    """Generate a mock Pantheon+ dataset matching v0.1's load_pantheonplus()
    return format.

    v0.1 uses keys: zHD, zHEL, mB, ceph, is_calib, N, Cinv. The likelihood
    chi2_panplus computes mu_pred from (zHD, zHEL) for non-calibrators or
    uses ceph directly for Cepheid hosts, then forms residual d = mB - mu_pred
    and contracts with Cinv (with M_B profiled analytically).

    To mock self-consistently, we compute the truth mu_pred at each SN, draw
    Gaussian noise from the full STAT+SYS covariance, and store the noisy
    magnitudes back into mB. Calibrators keep their ceph values unchanged
    (the calibration anchor is the SH0ES distance ladder, mocked separately
    via the SH0ES H0 prior).
    """
    pp = load_pantheonplus()

    zHD = pp["zHD"]
    zHEL = pp["zHEL"]
    is_calib = pp["is_calib"]
    ceph = pp["ceph"]
    Cinv = pp["Cinv"]
    n = pp["N"]

    H0 = truth_params["H0"]
    Om = truth_params["Om"]
    DC_grid = comoving_dist_grid(H0, Om, T_truth)
    if DC_grid is None:
        raise RuntimeError("comoving_dist_grid returned None at truth params")

    DM_at_zHD = np.interp(zHD, _Z_GRID, DC_grid)
    # Predicted mu: calibrators take ceph anchor, others take 5log10(dL)+25
    mu_pred = np.where(
        is_calib,
        ceph,
        5.0 * np.log10((1.0 + zHEL) * DM_at_zHD) + 25.0,
    )

    # The truth m_B before noise: m_B = mu_pred + M_B. We adopt M_B = -19.253
    # (standard SH0ES anchor); since chi2_panplus profiles M_B analytically,
    # the absolute value drops out and only the residual structure matters.
    M_B_truth = -19.253
    mB_truth = mu_pred + M_B_truth

    # Draw correlated noise from the full STAT+SYS covariance C = inv(Cinv).
    # For numerical stability we solve L @ L.T = C via Cholesky on the
    # explicit inverse. The 1701x1701 inversion is one-time per fit and
    # cached implicitly by Cholesky reuse upstream.
    cov = np.linalg.inv(Cinv)
    cov = 0.5 * (cov + cov.T)  # symmetrize against round-off
    # Tiny ridge for positive-definiteness under finite-precision inversion
    L = np.linalg.cholesky(cov + 1e-12 * np.eye(n))
    eps = L @ rng.standard_normal(n)

    pp_mock = dict(pp)
    pp_mock["mB"] = mB_truth + eps
    return pp_mock


def _build_mock_bao_tables(truth_params, T_truth, rng):
    """Generate mock BAO tables matching v0.1's data-tuple format.

    Returns dict {var_name: new_table} keyed by v0.1 module variable names.
    """
    H0 = truth_params["H0"]
    Om = truth_params["Om"]
    Obh2 = truth_params["Obh2"]
    rd = _derive_rd(H0, Om, Obh2, T_truth)
    Z_GRID = _Z_GRID
    DC_grid = comoving_dist_grid(H0, Om, T_truth)
    H_grid = H0 * E_lcdm(Z_GRID, Om)
    if T_truth is not None:
        H_grid = H_grid * T_truth(Z_GRID)

    def DM_at(z): return float(np.interp(z, Z_GRID, DC_grid))
    def DH_at(z): return C_KMS / float(np.interp(z, Z_GRID, H_grid))
    def DV_at(z):
        DM = DM_at(z)
        Hv = float(np.interp(z, Z_GRID, H_grid))
        return (z * DM ** 2 * C_KMS / Hv) ** (1.0 / 3.0)

    out = {}

    def patch_pair_table(orig):
        new = []
        for entry in orig:
            label, z, _DMo, sDM, _DHo, sDH, corr = entry
            mean = np.array([DM_at(z) / rd, DH_at(z) / rd])
            cov = np.array([[sDM ** 2, corr * sDM * sDH],
                            [corr * sDM * sDH, sDH ** 2]])
            L = np.linalg.cholesky(cov + 1e-12 * np.eye(2))
            eps = L @ rng.standard_normal(2)
            new.append((label, z, mean[0] + eps[0], sDM,
                        mean[1] + eps[1], sDH, corr))
        return new

    def patch_dv_table(orig):
        new = []
        for entry in orig:
            if len(entry) == 3:
                z, _DVo, sig = entry
                noise = sig * rng.standard_normal()
                new.append((z, DV_at(z) / rd + noise, sig))
            else:
                label, z, _DVo, sig = entry
                noise = sig * rng.standard_normal()
                new.append((label, z, DV_at(z) / rd + noise, sig))
        return new

    out["DESI_DR2_PAIR"]  = patch_pair_table(DESI_DR2_PAIR)
    out["DESI_DR2_DV"]    = patch_dv_table(DESI_DR2_DV)
    out["DESI_DR1_PAIR"]  = patch_pair_table(DESI_DR1_PAIR)
    out["DESI_DR1_DV"]    = patch_dv_table(DESI_DR1_DV)
    out["EBOSS_DM_PAIRS"] = patch_pair_table(EBOSS_DM_PAIRS)
    out["EBOSS_DV"]       = patch_dv_table(EBOSS_DV)

    # BOSS is split between BOSS_DM and BOSS_DH in v0.1
    boss_dm_new, boss_dh_new = [], []
    for (z1, _DMo, sDM), (z2, _DHo, sDH, corr) in zip(BOSS_DM, BOSS_DH):
        mean = np.array([DM_at(z1) / rd, DH_at(z2) / rd])
        cov = np.array([[sDM ** 2, corr * sDM * sDH],
                        [corr * sDM * sDH, sDH ** 2]])
        L = np.linalg.cholesky(cov + 1e-12 * np.eye(2))
        eps = L @ rng.standard_normal(2)
        boss_dm_new.append((z1, mean[0] + eps[0], sDM))
        boss_dh_new.append((z2, mean[1] + eps[1], sDH, corr))
    out["BOSS_DM"] = boss_dm_new
    out["BOSS_DH"] = boss_dh_new

    return out


@contextlib.contextmanager
def _patched_v01(bao_overrides=None, dp_overrides=None):
    """Temporarily replace BAO tables and DP target means at module scope.
    In the merged standalone file, the "backend" is this module itself."""
    self_mod = sys.modules[__name__]
    saved = {}
    if bao_overrides is not None:
        for name, new in bao_overrides.items():
            saved[name] = getattr(self_mod, name)
            setattr(self_mod, name, new)
    if dp_overrides is not None:
        saved["R_PLANCK"]  = self_mod.R_PLANCK
        saved["LA_PLANCK"] = self_mod.LA_PLANCK
        saved["WB_PLANCK"] = self_mod.WB_PLANCK
        self_mod.R_PLANCK  = dp_overrides[0]
        self_mod.LA_PLANCK = dp_overrides[1]
        self_mod.WB_PLANCK = dp_overrides[2]
    try:
        yield
    finally:
        for name, old in saved.items():
            setattr(self_mod, name, old)


def generate_mock(truth_params, seed=0, lvc_truth=True, with_pp=True):
    """Generate a complete synthetic dataset.

    Returns:
        bao_overrides : dict for v0.1 module-variable monkey-patching
        pp_mock       : mock Pantheon+ dict (or None)
        dp_mock       : mock distance-prior 3-vector
        h0_mock       : mock SH0ES H0 measurement (scalar)
    """
    rng = np.random.default_rng(seed)

    H0 = truth_params["H0"]
    A = truth_params["A"]

    if lvc_truth and A != 0:
        T_truth = lambda z: T_F2_theta_gaussian(z, A, THETA_C_LVC, W_LVC)
    else:
        T_truth = None  # treat as identity in v0.1's convention

    bao_overrides = _build_mock_bao_tables(truth_params, T_truth, rng)
    pp_mock = _build_mock_pp(truth_params, T_truth, rng) if with_pp else None

    # DP mock: target should be the model prediction at truth cosmology +
    # truth modulation, not the published Planck means. This keeps the mock
    # self-consistent with the LVC truth.
    _, R_truth, lA_truth = chi2_DP(H0, truth_params["Om"],
                                       truth_params["Obh2"], T_truth)
    dp_truth_means = np.array([R_truth, lA_truth, truth_params["Obh2"]])
    L_dp = np.linalg.cholesky(COV_DP)
    dp_mock = dp_truth_means + L_dp @ rng.standard_normal(3)

    h0_mock = H0 + SIG_H0_SHOES * rng.standard_normal()

    return bao_overrides, pp_mock, dp_mock, h0_mock


