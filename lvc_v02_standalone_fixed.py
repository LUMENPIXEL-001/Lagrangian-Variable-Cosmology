def T_F1_z_gaussian(z, A, zc, w):
    """F1: T(z) = 1 + A * exp(-((z-zc)/w)^2)."""
    return 1.0 + A * np.exp(-((np.asarray(z) - zc) / w) ** 2)


def T_F2_theta_gaussian(z, A, theta_c, w):
    """F2: T(z) = 1 + A * exp(-((theta-theta_c)/w)^2), theta = ln(1+z).
    The v0.1 functional form."""
    theta = np.log(1.0 + np.asarray(z))
    return 1.0 + A * np.exp(-((theta - theta_c) / w) ** 2)


def T_F3_skewed_z(z, A, zc, w_lo, w_hi):
    """F3: piecewise-Gaussian with separate widths below/above zc."""
    z = np.asarray(z, dtype=float)
    w = np.where(z < zc, w_lo, w_hi)
    return 1.0 + A * np.exp(-((z - zc) / w) ** 2)


def T_F4_double_z(z, A1, zc1, w1, A2, zc2, w2):
    """F4: two separate z-Gaussian bumps."""
    z = np.asarray(z, dtype=float)
    return 1.0 + A1 * np.exp(-((z - zc1) / w1) ** 2) \
               + A2 * np.exp(-((z - zc2) / w2) ** 2)


def T_F5_monotonic(z, A, zt, w):
    """F5: monotonic tanh transition."""
    z = np.asarray(z, dtype=float)
    return 1.0 + A * np.tanh((z - zt) / w)


def T_F6_narrow_z(z, A, zc, w):
    """F6: z-Gaussian with width restricted to w < 0.05 by bounds."""
    return T_F1_z_gaussian(z, A, zc, w)


