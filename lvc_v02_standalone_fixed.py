def chi2_mock(params, family_name, bao_overrides, pp_mock, dp_mock, h0_mock,
              include_pp=True):
    """Total chi^2 for a given family fit on the synthetic dataset."""
    H0, Om, Obh2 = params[:3]
    T_callable = build_T_callable(family_name, params[3:])

    chi2 = 0.0

    with _patched_v01(bao_overrides=bao_overrides, dp_overrides=dp_mock):
        rd = _derive_rd(H0, Om, Obh2, T_callable)
        chi2 += chi2_bao_combined(H0, Om, T_callable, rd)
        # chi2_DP returns (chi2, R_model, lA_model); we only need the chi2
        dp_result = chi2_DP(H0, Om, Obh2, T_callable)
        chi2 += dp_result[0] if isinstance(dp_result, tuple) else dp_result
        chi2 += ((H0 - h0_mock) / SIG_H0_SHOES) ** 2
        if include_pp and pp_mock is not None:
            pp_result = chi2_panplus(H0, Om, T_callable, pp_mock)
            chi2 += pp_result[0] if isinstance(pp_result, tuple) else pp_result

    return float(chi2)


