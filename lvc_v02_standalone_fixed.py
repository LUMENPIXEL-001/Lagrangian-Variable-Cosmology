def fit_family(bao_overrides, pp_mock, dp_mock, h0_mock,
               family_name, include_pp=True, n_seeds=3,
               de_iter=80, popsize=15, polish=True, verbose=False):
    """DE + Nelder-Mead fit of a functional family to the mock data."""
    fam_label, n_shape, shape_bounds = FAMILIES[family_name]
    bounds = list(COSMO_BOUNDS) + list(shape_bounds)

    def neg_lnL(p):
        try:
            return chi2_mock(p, family_name, bao_overrides,
                             pp_mock, dp_mock, h0_mock, include_pp=include_pp)
        except Exception:
            return 1e10

    best_chi2 = np.inf
    best_x = None
    t0 = time.time()
    for s in range(n_seeds):
        result = differential_evolution(
            neg_lnL, bounds,
            seed=s, maxiter=de_iter, popsize=popsize,
            tol=1e-7, mutation=(0.5, 1.5), recombination=0.7,
            polish=False, init='sobol')
        if polish:
            result = minimize(neg_lnL, result.x, method='Nelder-Mead',
                              options={'xatol': 1e-5, 'fatol': 1e-4,
                                       'maxiter': 2000})
        if result.fun < best_chi2:
            best_chi2 = result.fun
            best_x = result.x
        if verbose:
            print(f"    seed {s}: chi2 = {result.fun:.3f}")

    return dict(family=family_name, chi2=float(best_chi2),
                params=best_x.tolist(), time=time.time() - t0)


