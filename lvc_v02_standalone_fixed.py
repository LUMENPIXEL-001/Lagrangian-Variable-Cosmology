def _walk_for_json(d):
    """Recursively cast numpy types to native Python for JSON serialisation."""
    if isinstance(d, dict):
        return {k: _walk_for_json(v) for k, v in d.items()}
    if isinstance(d, list):
        return [_walk_for_json(x) for x in d]
    if isinstance(d, np.ndarray):
        return d.tolist()
    if isinstance(d, np.floating):
        return float(d)
    if isinstance(d, np.integer):
        return int(d)
    return d


